                    href="https://docs.folivora.ai/docs/1004_hyper_key.html">hyper key feature</a>. I use the hyper key
                in conjunction with Raycast so that I can launch my most often used apps via shortcuts. For example,
                <code>hyper + t</code> will launch or focus my Terminal window, <code>hyper + i</code> will open or
                focus Intellij, etc.</p>
        </li>
        <li>
            <div><a href="https://cleanshot.com/" target="_blank" rel="noopener noreferrer">CleanShot</a> <span
                    class="inline-block ml-1 text-xs py-1 px-2 rounded-full bg-gray-100 dark:bg-gray-800">Paid</span>
            </div>
            <p>CleanShot quickly replaced MacOS's built in screenshot tooling for me. It's a one stop shop and has
                everything I need for sharing what I'm working on with my team.</p>
        </li>
        <li>
            <div><a href="https://www.gitify.io" target="_blank" rel="noopener noreferrer">Gitify</a> <span
